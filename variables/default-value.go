// defining default values for variables

package main

import "fmt"

func main() {
	var num1 float32
	var num2 float64
	var num3 int
	var num4 int16
	var letter string
	var married bool

	fmt.Printf("num1 : %f\n", num1)
	fmt.Printf("num2 : %f\n", num2)
	fmt.Printf("num3 : %d\n", num3)
	fmt.Printf("num4 : %d\n", num4)
	fmt.Printf("letter : %s\n", letter)
	fmt.Println(married)
}
